
package com.tinesoft.gwt.dialogs.client.progress.core;

/**
 * An abstract adapter class for handling dialog box events defined in
 * {@link ProgressDialogEventListener}.
 * 
 * The methods in this class are empty. This class is provided as a convenience for easily creating
 * listeners by extending this class and overriding only the methods of interest.
 * 
 * @author Tine Kondo<kondotine@gmail.com>
 */
public abstract class ProgressDialogEventAdapter implements ProgressDialogEventListener {

    @Override
    public void onCloseButtonClicked() {

    }

    @Override
    public void onCancelButtonClicked() {

    }

 }
