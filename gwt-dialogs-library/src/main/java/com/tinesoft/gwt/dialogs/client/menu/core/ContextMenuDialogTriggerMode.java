
package com.tinesoft.gwt.dialogs.client.menu.core;

/**
 * Defines what event (on its target), triggers the display of the context menu dialog.
 * 
 * @author Tine Kondo<kondotine@gmail.com>
 * @version $Id$
 */
public enum ContextMenuDialogTriggerMode {

    /**
     * The context menu dialog is displayed when the mouse is over its target widget.
     */
    OnMouseOver,

    /**
     * The context menu dialog is displayed when a left click occurred on its target widget.
     */
    OnLeftClick

}
