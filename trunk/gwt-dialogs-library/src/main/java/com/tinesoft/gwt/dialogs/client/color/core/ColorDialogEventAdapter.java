
package com.tinesoft.gwt.dialogs.client.color.core;

/**
 * An abstract adapter class for handling color dialogs events defined in
 * {@link ColorDialogEventListener}.
 * 
 * The methods in this class are empty. This class is provided as a convenience for easily creating
 * listeners by extending this class and overriding only the methods of interest.
 * 
 * @author Tine Kondo<kondotine@gmail.com>
 * @version $Id$
 */
public abstract class ColorDialogEventAdapter implements ColorDialogEventListener {

    @Override
    public void onCloseButtonClicked() {

    }

    @Override
    public void onOkButtonClicked() {

    }

    @Override
    public void onCancelButtonClicked() {

    }

}
